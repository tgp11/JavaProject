
package Modelo;
public class Productora {
    private int idProductora;
    private int nPremios;
    private String nombre;
    private Integer productora2;

    public int getIdProductora() {
        return idProductora;
    }

    public void setIdProductora(int idProductora) {
        this.idProductora = idProductora;
    }

    public int getNPremios() {
        return nPremios;
    }

    public void setNPremios(int nPremios) {
        this.nPremios = nPremios;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public Integer getProductora2() {
        return productora2;
    }

    public void setProductora2(Integer productora2) {
        this.productora2 = productora2;
    }
}